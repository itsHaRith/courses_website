let currentTotal = 0;
function animateTotalValue(newTotal) {
    if (!cartTotalElement) return;
    const duration = 500;
    const start = currentTotal;
    const startTime = performance.now();
    
    function updateCounter(currentTime) {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);
        cartTotalElement.textContent = Math.floor(start + (newTotal - start) * progress);
        
        if (progress < 1) {
            requestAnimationFrame(updateCounter);
        } else {
            currentTotal = newTotal;
        }
    }
    
    requestAnimationFrame(updateCounter);
}

function generateCheckoutQR() {
    if (cart.length === 0) {
        showToast("السلة فارغة!", "error");
        return;
    }

    const total = cart.reduce((sum, item) => sum + item.price, 0);
    const date = new Date().toLocaleString('ar-EG');
    let textData = currentLang === 'en' ? `Order:\n` : `الطلب:\n`;
    cart.forEach((item, index) => {
        textData += `${index + 1}. ${item.title} ($${item.price})\n`;
    });
    
    const totalText = currentLang === 'en' ? `\nTotal: $${total}\nDate: ${date}` : `\nالإجمالي: $${total}\nالتاريخ: ${date}`;
    textData += totalText;

    const encodedData = encodeURIComponent(textData);
    const qrUrl = `https://api.qrserver.com/v1/create-qr-code/?size=250x250&data=${encodedData}`;
    showQRModal(qrUrl);
}

