function translateNode(node, lang) {
    if (node.tagName === 'SCRIPT') return;
    if (node.nodeType === 3) {
        const text = node.nodeValue.trim();
        if (text) {
            if (lang === 'en' && arToEn[text]) {
                node.nodeValue = node.nodeValue.replace(text, arToEn[text]);
            } else if (lang === 'ar' && enToAr[text]) {
                node.nodeValue = node.nodeValue.replace(text, enToAr[text]);
            }
        }
    } else if (node.nodeType === 1) {
        if (node.tagName === 'INPUT' && node.placeholder) {
            const text = node.placeholder.trim();
            if (lang === 'en' && arToEn[text]) {
                node.placeholder = arToEn[text];
            } else if (lang === 'ar' && enToAr[text]) {
                node.placeholder = enToAr[text];
            }
        }
        node.childNodes.forEach(child => translateNode(child, lang));
    }
}

function applyLanguage(lang) {
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    if (langBtn) langBtn.textContent = lang === 'ar' ? 'EN' : 'AR';
    translateNode(document.body, lang);
}

